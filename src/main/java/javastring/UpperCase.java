package javastring;

import java.util.Locale;

public class UpperCase {
    public static void main(String[] args) {
        String s1 = "akshay";
        String s2 = "JADHAV";
        String s3 = "    AKSHAY   ";
        System.out.println(s1.toUpperCase());      //Java String To UpperCase()
        System.out.println(s2.toLowerCase());      //Java String To LowerCase()
        System.out.println(s3.trim());             //Java String Trim()
    }
}
